from lammps import lammps

# Initialize LAMMPS
lmp = lammps()
lmp.command("units metal")
lmp.command("atom_style atomic")
lmp.command("boundary p p p")
lmp.command("read_data data.txt")
lmp.command("region down block 0 30 0 30 0 15")
lmp.command("region up block 0 30 0 30 25 29")
lmp.command("group 1 type 1")
lmp.command("group 2 type 2")
lmp.command("group 3 type 3")
lmp.command("group down region down")
lmp.command("group up region up")
lmp.command("pair_style hybrid morse 15.0 eam/fs")
lmp.command("pair_coeff * * eam/fs Ti.eam.fs Ti NULL NULL")
lmp.command("pair_coeff 1 3 morse 15.0 0.00567139 1.5543 4.18784")
lmp.command("pair_coeff 1 2 morse 15.0 1.279493 3.640737 1.88265")
lmp.command("pair_coeff 2 2 morse 15.0 0.042117 1.1861 3.70366")
lmp.command("pair_coeff 2 3 morse 15.0 1.279493 3.640737 1.88265")
lmp.command("pair_coeff 3 3 morse 15.0 0.00567139 1.5543 4.18784")
lmp.command("velocity 1 set 0 0 0")
lmp.command("velocity 2 set 0 0 -6")
lmp.command("neighbor 1.0 bin")
lmp.command("neigh_modify every 1 delay 5 check yes")
lmp.command("fix 1 all nve")
lmp.command("timestep 0.001")
lmp.command("thermo_style custom step pe press temp")
lmp.command("thermo 100")
lmp.command("thermo_modify lost ignore flush yes")
lmp.command("run 1000000")

# Close LAMMPS
lmp.close()
